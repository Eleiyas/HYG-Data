local function get_cs_class(class_name)
  local strs = lua_str_split(class_name, ".")
  
  local cs_class = CS
  for _, str in ipairs(strs) do
    cs_class = cs_class[str]
  end
  return cs_class
end

local function ensure_class_ins(cls)
  if rawget(cls, "class_ins") == nil then
    rawset(cls, "class_ins", get_cs_class(cls.class_name))
  end
  if rawget(cls, "class_ins") == nil then
    Logger.LogError("CS class " .. cls.class_name .. " not found")
    return false
  end
  return true
end

local cs_metatable = {
  __newindex = function(cls, key, value)
    if ensure_class_ins(cls) == false then
      return
    end
    rawget(cls, "class_ins")[key] = value
  end,
  __index = function(cls, key)
    if ensure_class_ins(cls) == false then
      return nil
    end
    return rawget(cls, "class_ins")[key]
  end,
  __call = function(cls, ...)
    if ensure_class_ins(cls) == false then
      return nil
    end
    return rawget(cls, "class_ins")(...)
  end,
  __type = function(cls)
    if ensure_class_ins(cls) == false then
      return nil
    end
    return rawget(cls, "class_ins").__type
  end
}

local function cs_define(class_name)
  local cs_class = {}
  cs_class.class_name = class_name
  setmetatable(cs_class, cs_metatable)
  return cs_class
end

